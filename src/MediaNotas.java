public class MediaNotas {
    public static void main(String[] args) {
        double nota1 = 10;
        double nota2 = 9;
        double nota3 = 8;
        double nota4 = 7;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.printf("A média das notas é: %.2f\n", media);
    }
}